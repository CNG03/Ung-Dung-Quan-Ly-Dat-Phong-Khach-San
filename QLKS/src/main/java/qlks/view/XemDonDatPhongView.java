    public void showTable() throws IOException, ClassNotFoundException {
        DatPhongDao dpd = new DatPhongDao();
        List<Datphong> ddp = dpd.readDatPhongFromFile();
        if(ddp==null) {
            JOptionPane.showMessageDialog(this, "bi null");
            return;
        }
        DefaultTableModel model = (DefaultTableModel) donDatPhongjTable.getModel();
        for (Datphong dp : ddp) {
            if (dp != null) {
                List<Phong> dsp = dp.getPh();
                String strNumbers = "";
                for (int i = 0; i < dsp.size(); i++) {
                    if (i > 0) {
                        strNumbers += ", ";
                    }
                    strNumbers += Integer.toString(dsp.get(i).getSoPhong());
                }

            Object[] row = new Object[5];
            row[0] = dp.getIdDatPhong();
            row[1] = dp.getKH().getHoten();
            row[2] = "0"+dp.getKH().getSdt();
            row[3] = strNumbers;
            row[4] = dp.getThoiGianDatPhong();
            model.addRow(row);
        }
    }

    }
    
    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) donDatPhongjTable.getModel();
        model.setRowCount(0);
    }
    
    public boolean checkNgay() throws IOException, ClassNotFoundException {
        String maDP = getSelectedRowData();
        DatPhongDao dpd = new DatPhongDao();
        List<Datphong> dsDP = dpd.readDatPhongFromFile();
        Date ngayHienTai = new Date();
        for(Datphong d : dsDP) {
            if(d.getIdDatPhong().equals(maDP)) {
                Date ngayDatPhong = d.getThoiGianDatPhong();
                if (ngayHienTai.compareTo(ngayDatPhong) > 0) {
                    System.out.println("ngayhientai lớn hơn ngaydatphong");
                    return true;
                } else {
                    System.out.println("ngayhientai nho hon ngay dat phong");
                    return false;
                }
            }
        }
        return false; // vi khong co phong nao duoc chon
        //ham chi hoat dong dung khi co dong duoc chon nho dieu nay phai long vao trong dieu kien cua su kien nhan button nha
    }
    
    public void traPhong(Datphong dp) throws IOException, ClassNotFoundException {
        List<Phong> dsPhong = dp.getPh();
        ThemPhongDao tp = new ThemPhongDao();
        List<Phong> dsAllPhong = tp.readPhongFromFile();
        for(Phong a : dsAllPhong) {
            for(Phong b : dsPhong) {
                if(b.getSoPhong()==a.getSoPhong()) {
                    a.setDaDuocDat(false);
                }
            }
        }
        tp.saveDanhSachPhong(dsAllPhong);
    }
    
