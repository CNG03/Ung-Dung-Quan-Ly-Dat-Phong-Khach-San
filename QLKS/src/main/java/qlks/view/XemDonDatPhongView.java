    public void xoaDonDatPhong(Datphong dp) throws IOException, ClassNotFoundException {
        DatPhongDao dpd = new DatPhongDao();
        List<Datphong> donDatPhong = dpd.readDatPhongFromFile();
        if (donDatPhong.size() == 1 && donDatPhong.contains(dp)) {
            donDatPhong.clear();
        } else {
            donDatPhong.remove(dp);
        }
                    Iterator<Datphong> iterator = donDatPhong.iterator();
                    while (iterator.hasNext()) {
                        Datphong p = iterator.next();
                        if (p.getIdDatPhong().equals(dp.getIdDatPhong())) {
                            iterator.remove();
                        }
                    }
        dpd.saveListDatPhong(donDatPhong);
    }

    
