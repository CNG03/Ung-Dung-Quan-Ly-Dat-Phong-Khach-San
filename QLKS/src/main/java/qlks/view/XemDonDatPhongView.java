    public boolean isSelectedRow() {
        if (donDatPhongjTable.getSelectedRow() == -1) {
    // Không có dòng nào được chọn trong bảng
            return false;
        } else {
            // Đã có dòng được chọn trong bảng
            return true;
        }
    }
    
    public String getSelectedRowData() {
        int selectedRowIndex = donDatPhongjTable.getSelectedRow();

        if (selectedRowIndex < 0) {
            // Không có hàng nào được chọn
            return "";
        }

        DefaultTableModel model = (DefaultTableModel) donDatPhongjTable.getModel();
        Object rowData = model.getValueAt(selectedRowIndex, 0);

        // Dữ liệu của cột đầu tiên của hàng được chọn
        
        return rowData.toString();
    }
    
    public Datphong layDonDatPhong() throws IOException, ClassNotFoundException {
        String maDP = getSelectedRowData();
        DatPhongDao dpd = new DatPhongDao();
        List<Datphong> dsp = dpd.readDatPhongFromFile();
        for(Datphong d : dsp) {
            if(maDP.equals(d.getIdDatPhong())) {
                return d;
            }
        }
        return null;
    }
    
    
    
    
